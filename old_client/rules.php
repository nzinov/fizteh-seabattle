<p align="center">НЕсамостоятельные фишки.</p>
<img src="rules/dependent.png" class="img-responsive" alt="" />
<p><strong><em>Мина (сокр. - Мн)</em></strong> - фишка ходящая рядом с <strong>Эсминцем</strong>. Может передвигаться только по 8 клеткам вокруг <strong>Эс</strong> (в том числе и диагональным). Может передаваться с &laquo;поля ответственности&raquo; одного <strong>Эс</strong> к другому <strong>Эс</strong>. При атаке <strong>Мн</strong> кораблём противника взрывается вместе с ним (за исключением <strong>Тральщика</strong>). Сама атаковать и стрелять не может (!).</p>
<p><strong><em>Торпеда (сокр.- Т)</em></strong> - аналогично, ходит только рядом с <strong>Тк</strong>. Может ходить на 2 клетки, как <strong>Тк</strong>. При атаке кораблём, взрывается как <strong>Мина</strong>, также тралится <strong>Тральщиком</strong>. Может стрелять, если рядом находится <strong>Тк</strong>. При выстреле <strong>Т</strong> поражает любую фишку (кроме <strong>Форта</strong>) на расстоянии не более чем 4 клетки и сама уничтожается. Бьёт только по прямой в любом направлении. Через корабли не прыгает, поэтому может поразить только ближайший по прямой корабль.</p>
<p><strong><em>Самолёт (сокр. - См</em></strong> ) - ходит только рядом с <strong>Ав</strong>. Стреляет, если рядом есть<strong> Ав</strong>, при этом уничтожает любую фишку (кроме <strong>Форта</strong>) на любом расстоянии по прямой (в любом направлении, даже по диагонали), при этом погибает сам. Летает над кораблями. При атаке противником взрывается уничтожая любой(!!!) корабль в том числе и <strong>Тр</strong>.</p>
<p>&nbsp;</p>
<p><strong><em>КрПл-ная Ракета (сокр. - Рк ) </em></strong> - ходит только вокруг <strong>КрПл</strong>, стреляет тоже, если рядом <strong>КрПл</strong>. Выстрелить<strong> Рк</strong> может одним из двух способов:</p>
<ol>
<li><strong>Ракета</strong> запускается на расстояние не более 2-х клеток по прямой (над кораблями, как и <strong>См</strong>), при попадании поражает квадрат 3х3 вокруг себя</li>
<li><strong>Ракета</strong> запускается по прямой не более, чем на 3 клетки (над кораблями) и поражает только корабль, в который попала.</li>
</ol>
<p>чтобы выбрать способ, которым использовать <strong>Рк</strong> нажмите R при включенном режиме выстрела (подсказка показывается на панели состояния)</p>
<p><strong><em>Транспорт (сокр. &ndash; Тп)</em></strong> &ndash; самый слабый из кораблей, бьётся даже Пл. Особое свойство &ndash; может перемещать ЛЮБЫЕ несамостоятельные фишки: мины, торпеды, самолеты, ракеты&hellip;(форты, конечно, не двигаются). Т.е. рядом с ней все эти фишки могут двигаться не имея &laquo;родных&raquo; носителей.</p>
<p align="center">НЕподвижные фишки.</p>
<p><strong><em>Форт (сокр.- Ф) </em></strong> <strong>- Форт</strong> единственная абсолютно неподвижная вфишка. В сущности, это просто флаг. Он не может ничего - просто стоит и ждёт, пока его возьмут. <strong>Форт</strong> можно взять любой спрашивающей фишкой, его можно уничтожить <strong>АБ</strong>, его можно захватить <strong>Брандером</strong> или <strong>Нейтронной Бомбой</strong>. Внимание: <strong>Торпеды</strong> и <strong>Самолёты</strong> вреда <strong>Форту</strong> не наносят. Форты в начале игры расставляются на произвольные позиции, где и находятся до конца игры или пока их не возьмут.</p>
