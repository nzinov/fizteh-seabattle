<p align="center">Таб.1.1</p>
<p>Все перечисленные фишки образуют строгую вертикаль, где каждый корабль следующего по старшинству ранга ровно в 1,5 раза слабее предыдущего. Т. е. <strong>Ав = 1,5Лк = 1,5*1,5Кр = 1,5*1,5*1,5Эс = 1,5*1,5*1,5*1,5Ст = &hellip;</strong> и т. д.</p>
<p>Но есть фишка, находящаяся между рангов - это <strong>Рейдер</strong>. <strong>Рейдер</strong> - промежуточная между <strong>Кр</strong> и <strong>Эс</strong> фишка, способная образовывать блоки, как между собой, так и с младшими фишками.</p>
<p><strong>Рейдер (сокр. - Рд)</strong> - следующая по рангу за Крейсером фишка, один на один бьёт всех кроме <strong>Ав</strong>, <strong>Лк и Кр</strong>. Бьётся блоками <strong>2Эс</strong> и<strong>3Ст</strong>. По силе в точности равен <strong>2Ст</strong>. <strong>Рд = 2Ст = 3Тк</strong>. <strong>Рд </strong>может создавать блоки с любыми фишками младших классов (естественно, только теми, которые могут образовывать блоки, то есть <strong>Эс, Ст, Тк и Тр</strong>). В таком блоке <strong>Рд </strong>притворяется стоящей рядом фишкой, например, если рядом стоит <strong>Тк</strong>, то такой блок может объявить себя как <strong>2Тк</strong> (а, если рядом ещё один <strong>Тк</strong>, то соответственно,<strong> 3Тк</strong>). Такие комбинации обычно используются, когда вашу фишку атакует более сильная, чтобы защитить её. Но, учтите, что будет очень обидно разменять, например, <strong>2Тк+Рд</strong> (объявленные <strong>3Тк</strong>) на <strong>2Ст</strong>! Особым свойством<strong> Рд</strong>, является также то, что<strong> Рд</strong> способен создавать блок со <strong>Ст</strong> обозначаемый <strong>2 Эс</strong>, также может образовывать в целях маскировки блок со <strong>Ст</strong> - <strong>2 Ст</strong> (но учтите: если<strong> Рд</strong> в таком блоке атакует <strong>Крейсер</strong>, то <strong>Рейдер</strong> будет уничтожен, хотя если бы объявил себя <strong>2 Эсминцами</strong> &laquo;съел&raquo; бы <strong>Крейсера</strong>). Так же <strong>Рд&nbsp;</strong>может образовать блок из 3-х кораблей <strong>Рд-Ст-Эс</strong> (<strong>Рд-Ст-Рд</strong>)&nbsp; - объявляется <strong>3 Эс</strong>.</p>
<p align="center">Самостоятельно ходящие фишки, НЕ образующие блоки.</p>
<p><strong><em>Подводная Лодка (сокр. - Пл</em></strong> ) - охотница на <strong>Линкоры</strong>. Бьётся всеми кроме <strong>Лк</strong> (одиночного, или входящего в блок, роли не играет) и <strong>Тп</strong>.</p>
<p><strong>Крейсерская Подводная Лодка (сокр.- КрПл) </strong> - очень &laquo;крута&raquo;. Бьёт всех кроме <strong>Кр, Рд, Эс</strong>. Бьет фишки входящие в блоки, как отдельные. То есть, атаковав входящий в блок <strong>3 Эсминца</strong> <strong>Сторож</strong>, КрПл уничтожает его.</p>
<p><strong><em>Брандер (сокр. - Бр)</em></strong> - Может выстрелить в стоящий рядом корабль противника и таким образом захватить его. Это значит, что этот корабль становится вашим кораблём. После захвата ход остается у вас. Не может стрелять несколько раз подряд. Корабли, атакующие <strong>Брандер</strong>, взрываются вместе с ним, как на <strong>Мине</strong> (кроме <strong>Тральщика</strong>, естественно).</p>
<p><strong><em>Атомная Бомба (сокр. - АБ)</em></strong> - особая фишка, ходит как обычный корабль, не может атаковать, но может &laquo;взорваться&raquo;. При взрыве уничтожает все корабли и чужие, и свои в квадрате 5 на 5 клеток вокруг себя. Взрывается как при атаке чужим кораблём, так самостоятельно - &laquo;пошёл и взорвался&raquo; (для этого в фазу атаки нужно нажать на нее).</p>
<p><strong><em>Нейтронная Бомба (сокр. - НБ)</em></strong> - особая фишка, все правила игры <strong>НБ</strong> совпадают с правилами по <strong>АБ<em>. </em></strong>Единственное отличие - результат от взрыва. Если при взрыве <strong>АБ</strong> все фишки в квадрате 5х5 &laquo;уничтожаются&raquo;, то при взрыве <strong>НБ</strong> фишки в квадрате 5х5 становятся &laquo;ничейными&raquo;. Чтобы &laquo;захватить&raquo; такую &laquo;ничейную&raquo; фишку достаточно подойти к ней своей и атаковать её.</p>
