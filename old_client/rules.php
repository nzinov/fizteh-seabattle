<h3>Подробнее о фазе атаки: блоки</h3>
<p>Два корабля (фишки) одного типа стоящие рядом (не по диагонали!!!) образуют блок из двух кораблей. Если рядом с одним из них (опять же не по диагонали) стоит третий корабль, то получается блок из трёх кораблей (ясно, что тройной блок может стоять или &laquo;линией&raquo; или &laquo;уголком&raquo;). Блоков больше трёх кораблей не бывает. Не все типы кораблей могут образовывать блоки, а <strong>Рейдер</strong> может образовывать блоки не только с другими <strong>Рейдерами</strong>, но&nbsp; и с фишками других типов. (подробности будут дальше).</p>
<p>Во время атаки игрок выбирает какой-нибудь корабль противника как цель атаки. Противник должен выбрать, в какой блок входит его атакованный корабль. Так, например, если вы атаковали один из трех стоящих в линию рядышком <strong>Эсминцев</strong>, он может заявить блок из одного, двух, или трех эсминцев (так как не обязательно включать в блок рядом стоящие корабли). В панели сообщений появится что-то вроде "Противник заявил блок из 2-х Тральщиков". После этого выбирайте блок, в который входит корабль, которым вы атаковали. Если ваш блок сильнее - противник потеряет атакованную фишку, так и не узнав, каким блоком вы его атаковали. Если ваш блок слабее - вы теряете фишку, которой атаковали, и противник узнает, какой у вас был блок. А вот если Ваш блок и блок противника равны по силе, то и ваш блок (все корабли!) и блок противника полностью уничтожаются.</p>
<p>Когда вам нужно выделить блок, на панели состояния появляется надпись "Выберите блок", и атакующий и атакованный корабль выделяются синим и красным фоном соответсвенно. Слева появляется панель, разделенная на клетки, в клетке нарисован корабль и написана цифра. Щелкнув на эту клетку вы заявите блок из такого числа таких кораблей. Щелкните на какой-нибудь корабль, чтобы добавить его в блок - он тоже подсветится и панель блоков изменится. Щелкните еще раз, чтобы исключить корабль из блока.</p>
<p><strong>Пример:</strong> Крейсер стоящий по диагонали с другим Крейсером делает ход вставая рядом с ним (блок 2 Крейсера) и рядом с неизвестной фишкой противника, стоящей в ряд с ещё двумя другими. При этом Крейсер атакует фишку стоящую рядом с ним. атакованная фишка входит в блок 2 Эсминца&raquo;. Владелец крейсеров объявляет: &laquo;Был блок :-)&raquo; и, не объявляя своих фишек, снимает атакованного Эсминца. Далее владелец Крейсера по правилу &laquo;убил-ходишь&raquo; получает новый ход и, разрывая блок Крейсеров делает одним из них ход вперёд, атакую следующую фишку (он уверен, что там не более 1 Эсминца). Атакованный корабль отвечает: &laquo;Блок 2 Эсминца!&raquo;. &laquo;А я Крейсер&hellip;был :-(&raquo; - отвечает атаковавший и теряет Крейсер. Как такое могло получиться? Игрок с Эсминцами имел 3 Эсминца стоящие в ряд, когда одного из них (крайнего) атаковали 2 неизвестные фишки, то игрок имел право заявить его и как 1 Эсминец, и как блок 2 Эсминцев, и как блок 3 Эсминцев. Не предполагая, что это атака Крейсеров, игрок предпочёл заявить лишь 2 Эсминца и проиграл атакованную фишку. Когда же введённый в заблуждение атакующий разорвал свой блок и атаковал оставшихся 2-х Эсминцев одиночным Крейсером, то конечно же был &laquo;убит&raquo;. Стоит обратить внимание на нюанс: если бы атакованный заявил сразу блок 3 Эсминца, то у атакующего было бы 2 варианта: или заявить блок 2 Крейсера и произвести размен блоков, либо не &laquo;засвечивая&raquo; 2-го Крейсера пожертвовать первым, как одиночной фишкой. В зависимости от ситуации бывают выгодны оба варианта.</p>
