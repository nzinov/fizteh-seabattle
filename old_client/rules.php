<h2>Фишки</h2>
<p>Разных типов фишек довольно много, но многие из них не отличаются друг от друга ничем, кроме силы. Условно их можно разделить на несколько классов:</p>
<p align="center">Фишки, образующие блоки</p>
<p><strong><em>Авианосец (сокр. - Ав)</em></strong> - формально самая сильная фишка, один на один бьёт любую другую фишку кроме <strong>КрПл.</strong> Также бьётся блоками <strong>2-х Линкоров (Лк)</strong> или <strong>3-х Крейсеров (Кр).</strong> Способность &laquo;нести&raquo; рядом с собой <strong>Самолёт (См)</strong>, и производить &laquo;выстрел&raquo; им.</p>
<p><strong><em>Линкор (сокр. Лк</em></strong> <em>) -</em> следующая по рангу фишка, один на один бьёт всех кроме <strong>Ав</strong>, <strong>КрПл и Пл</strong>(!!!). Также бьётся блоками <strong>2-х Крейсеров (Кр)</strong> или <strong>3-х Эсминцев (Эс)</strong>. <strong>2Лк = 3Кр</strong>. <strong>3Лк = 2Ав</strong>. В наборе их всего по 2 штуки у каждого игрока.</p>
<p>&nbsp;</p>
<p><strong><em>Крейсер (сокр. - Кр) </em></strong> - следующая по рангу фишка, один на один бьёт всех кроме <strong>Ав</strong> и <strong>Лк</strong>. Бьётся блоками <strong>2-х Рейдеров (Рд</strong>) (!), <strong>2-х Эсминцев (Эс)</strong> или <strong>3-х Сторожей (Ст)</strong>. <strong>2Кр = 3Эс</strong>, <strong>3Кр = 2Лк</strong>.</p>
<p><strong><em>Эсминец (сокр. - Эс)</em></strong> - следующая по рангу фишка (если пропустить <strong>Рейдер (Рд)</strong>), один на один бьёт всех кроме <strong>Ав</strong> , <strong>Лк, Кр и Рд</strong> (!). Бьётся блоками<strong> 2Ст</strong> или <strong>3Тк</strong>. <strong>2Эс = 3Ст</strong>. <strong>3Эс = 2Кр</strong>.</p>
<p><strong><em>Сторож (сокр. - Ст)</em></strong> - следующая по рангу фишка, один на один бьёт всех кроме <strong>Ав</strong> , <strong>Лк, Кр, Рд, Эс и КрПл</strong>. Бьётся блоками <strong>2Тк</strong> или <strong>3Тр (Тральщика)</strong>. <strong>2Ст = 3Тк = 1Рд. 3Ст = 2Э.</strong></p>
<p><strong><em>Торпедный Катер (сокр. - Тк</em></strong> ) - следующая по рангу фишка, один на один бьёт всех кроме <strong>Ав</strong> , <strong>Лк, Кр, Рд, Эс, Ст и КрПл</strong>. Бьётся блоком <strong>2Тр.</strong> <strong>2Тк = 3Тр. 3Тк = 2Ст = 1Рд.</strong> Особые свойства: способен таскать рядом с собой <strong>Торпеды</strong> и запускать их; может двигаться, как по одной клетке в ход, так и по две (но не по диагонали!). Так, например, может сделать ход вперёд-вправо или вправо-вперёд по свободным клеткам, но, если справа и спереди стоят корабли, то занять клетку &laquo;проскользнув&raquo; между ними он не может!</p>
<p><strong><em>Тральщик ( сокр. - Тр)</em></strong> - следующая по рангу фишка, один на один бьёт всех кроме <strong>Ав</strong> , <strong>Лк, Кр, Рд, Эс, Ст, Тк и КрПл</strong>. То есть бьёт только<strong> Пл.</strong> <strong>3Тр=2Тк</strong>. При всей своей слабости, обладает неоценимым свойством - способен разминировать (уничтожать не погибая сам)<strong> Мины</strong>, <strong>Торпеды</strong> и <strong>Брандеры</strong>.</p>
