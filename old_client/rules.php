<h2>Основные правила</h2>
<p>В Морской бой по-физтеховски играют два игрока. Игра ведется на квадратном клетчатом поле 14 на 14 клеток. Оба игрока имеют одинаковые наборы специальных фишек, изображения на которых видны самому игроку, но не видны противнику. То есть каждый игрок видит свои фишки и положение - но не тип - фишек соперника. Перед началом игры игроки расставляют свои наборы фишек произвольным образом на первых 3-х линиях поля (друг напротив друга). После этого игроки по очереди совершают ходы. Игрок, который не может совершить ход, проигрывает</p>
<p>Во время своего хода игрок может передвинуть один из своих кораблей, а затем атаковать (тем же или другим кораблём) корабльпротивника. При этом он может пропустить, как перемещение, так и атаку, но не то и другое вместе. То есть, нельзя полностью пропустить ход. Заметьте, что переместить корабль (сходить) можно только до атаки, но не наоборот. Так же, вместо перемещения и атаки можно произвести выстрел подходящей фишкой. Это делается вместо всего хода. В случае, если игрок удачно произвел атаку или выстерл, то он получает дополнительный ход. Ходить фишки могут на одну клетку не по диагонали (за исключением <strong>Торпедных Катеров и Торпед</strong>, которые могут ходить на 2 клетки). Атаковать можно стоящий на соседней по стороне клетке корабль противника.</p>
<p>Цель игры уничтожить или захватить неподвижную фишку <strong>- Форт</strong> противника.</p>
<h2>Игровой экран</h2>
