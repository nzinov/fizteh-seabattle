<p>Одним из основных элементов является <strong>Панель состояния</strong>, которая расположена в правом верхнем углу игрового экрана. На ней написано, что вам нужно делать в данный момент, например, "Расставьте корабли", или "Ждите". Также на панели состояния есть описание текущей игры (кто и скем играет) и кнопки, позволяющие сдаться, или предложить ничью</p>
<p>Под панелью состояния расположена панель сообщений, там располагаются сообщения о ходе игры, как например "Ваш противник сходил" и сообщения, которые пишет вам ваш противник, например "Эх, погибла моя Непобедимая Армада ((". Чтобы ответить ему, достаточно нажать Enter, ввести в появившееся внизу окошко свое сообщение и снова нажать Enter. Да, кстати, если указатель мыши на сообщение "Ваш противник потерял Мину", то на поле подсветится клетка, на которой эта мина стояла - точно так же происходит с сообщением о любом событии в игре.</p>
